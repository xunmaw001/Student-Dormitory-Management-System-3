       this.$message.error(`年龄应输入整数`);
        return
      }
      if( 'suguan' ==this.flag && this.ruleForm.shouji&&(!isMobile(this.ruleForm.shouji))){
        this.$message.error(`手机应输入手机格式`);
        return
      }
