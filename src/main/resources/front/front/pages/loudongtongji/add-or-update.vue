		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
    		        this.ruleForm.dengjiriqi = this.$utils.getCurDate();
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取


			// 下2
			res = await this.$api.option(`loudongxinxi`,`loudongbianhao`,{});
			this.loudongbianhaoOptions = res.data;

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`loudongtongji`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='loudongbianhao'){
					this.ruleForm.loudongbianhao = obj[o];
					this.ro.loudongbianhao = true;
					continue;
					}
					if(o=='sushelouhao'){
					this.ruleForm.sushelouhao = obj[o];
					this.ro.sushelouhao = true;
					continue;
					}
					if(o=='zaiyongsushe'){
					this.ruleForm.zaiyongsushe = obj[o];
					this.ro.zaiyongsushe = true;
					continue;
					}
					if(o=='kongxiansushe'){
					this.ruleForm.kongxiansushe = obj[o];
					this.ro.kongxiansushe = true;
					continue;
					}
					if(o=='chuangweishuliang'){
					this.ruleForm.chuangweishuliang = obj[o];
					this.ro.chuangweishuliang = true;
					continue;
					}
					if(o=='kongchuangweishu'){
					this.ruleForm.kongchuangweishu = obj[o];
					this.ro.kongchuangweishu = true;
					continue;
					}
					if(o=='beizhu'){
