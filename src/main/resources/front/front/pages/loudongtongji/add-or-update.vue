			async onSubmitTap() {



















//跨表计算判断
				if(this.ruleForm.zaiyongsushe&&(!this.$validate.isIntNumer(this.ruleForm.zaiyongsushe))){
					this.$utils.msg(`在用宿舍应输入整数`);
					return
				}
				if(this.ruleForm.kongxiansushe&&(!this.$validate.isIntNumer(this.ruleForm.kongxiansushe))){
					this.$utils.msg(`空闲宿舍应输入整数`);
					return
				}
				if(this.ruleForm.chuangweishuliang&&(!this.$validate.isIntNumer(this.ruleForm.chuangweishuliang))){
					this.$utils.msg(`床位数量应输入整数`);
					return
				}
				if(this.ruleForm.kongchuangweishu&&(!this.$validate.isIntNumer(this.ruleForm.kongchuangweishu))){
					this.$utils.msg(`空床位数应输入整数`);
					return
				}
				//更新跨表属性
			       var crossuserid;
			       var crossrefid;
			       var crossoptnum;
				if(this.cross){
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
						var obj = uni.getStorageSync('crossObj');
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
						       crossuserid=Number(uni.getStorageSync('userid'));
						       crossrefid=obj['id'];
						       crossoptnum=uni.getStorageSync('statusColumnName');
						       crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`loudongtongji`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
						return false;
					} else {
                //跨表计算
						if(this.ruleForm.id){
							await this.$api.update(`loudongtongji`, this.ruleForm);
						}else{
							await this.$api.add(`loudongtongji`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`loudongtongji`, this.ruleForm);
					}else{
						await this.$api.add(`loudongtongji`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
