					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
			// 下二随
			async loudongbianhaoChange (e) {
				this.loudongbianhaoIndex = e.target.value
				this.ruleForm.loudongbianhao = this.loudongbianhaoOptions[this.loudongbianhaoIndex]
				let res = await this.$api.follow(`loudongxinxi`, `loudongbianhao`,{
